    function displayData(arr){
        document.querySelector("#pro_container").innerText = ""
        arr.map(function(el){
            var innerbox = document.createElement("div");
            innerbox.setAttribute("id","innerBox");
            innerbox.addEventListener("click",function(){
                window.location.href = "productPage.html"
              });
            var img = document.createElement("img");
            img.src = el.image;
    
            var name = document.createElement("p");
            name.setAttribute("class","product_name")
            name.innerText = el.name;
    
            var price = document.createElement("p");
            price.style.fontSize = "18px"
            price.innerText = el.price;
            price.style.color = "#f6575e"
    
            var stOffdiv = document.createElement("div");
            stOffdiv.setAttribute("id","stPrice")
            var tag = document.createElement("p");
            tag.innerText = "RRP :"
            tag.style.fontSize = "12px"
            tag.style.color = "#bec3c4"
            var soprice = document.createElement("p");
            soprice.style.textDecoration = "line-through"
            soprice.style.fontSize = "12px"
            soprice.style.color = "#bec3c4"
            
            soprice.innerText = el.strikeOffPrice;
            stOffdiv.append(tag,soprice);
    
            var cartIcon = document.createElement("div");
            cartIcon.style.display = "flex"
            cartIcon.style.justifyContent = "right"
            var iconimg = document.createElement("img")
            iconimg.src = el.iconImg
            cartIcon.append(iconimg);
            
    
            innerbox.append(img,name,price,stOffdiv,cartIcon);
            document.querySelector("#pro_container").append(innerbox);
        })
    }
